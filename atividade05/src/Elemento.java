public class Elemento {
    private int key;
    private Elemento next;

    public void setKey(int key) {
        this.key = key;
    }

    public void setNext(Elemento next) {
        this.next = next;
    }

    public int getKey() {
        return key;
    }

    public Elemento getNext() {
        return next;
    }
}
